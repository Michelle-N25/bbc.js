//regular array
let students = ['michelle', 'cedra', 'davis'];
//pop- pop()
students.pop();
console.log(students);

//push
students.push('musa', 'sarah');
console.log(students);

//shift()
 students.shift();
 console.log(students);

 //unshift
 students.unshift('elijah', 'edwin');
console.log(students);

//slice
students.slice('sarah', "edwin");
console.log(students);

